
public class DataType {
	/*
	 * 		데이터 타입
	 * 			정수 : 숫자(양수, 음수, 0)
	 * 			실수	: 소수점까지 저장하는 숫자(+ -)
	 * 			논리 : true false
	 */
	public static void main(String[] args) {
		//정수 출력 부분
		System.out.println(100 + 30);
		System.out.println(200+40);
		//실수 출력
		System.out.println(3.1415+10);
		//논리값
		System.out.println(true);
		System.out.println(false);
	}

}




